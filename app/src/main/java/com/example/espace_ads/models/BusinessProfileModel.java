package com.example.espace_ads.models;

public class BusinessProfileModel {
    String companyName, companyBio, reviewIndividuals, companyType, email, socialMedia, companyUrl, companyLogo, companyCoverImage;
    long reviewsNumber,engagements, sells, visits;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyBio() {
        return companyBio;
    }

    public void setCompanyBio(String companyBio) {
        this.companyBio = companyBio;
    }

    public String getReviewIndividuals() {
        return reviewIndividuals;
    }

    public void setReviewIndividuals(String reviewIndividuals) {
        this.reviewIndividuals = reviewIndividuals;
    }

    public String getCompanyType() {
        return companyType;
    }

    public void setCompanyType(String companyType) {
        this.companyType = companyType;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSocialMedia() {
        return socialMedia;
    }

    public void setSocialMedia(String socialMedia) {
        this.socialMedia = socialMedia;
    }

    public String getCompanyUrl() {
        return companyUrl;
    }

    public void setCompanyUrl(String companyUrl) {
        this.companyUrl = companyUrl;
    }

    public String getCompanyLogo() {
        return companyLogo;
    }

    public void setCompanyLogo(String companyLogo) {
        this.companyLogo = companyLogo;
    }

    public String getCompanyCoverImage() {
        return companyCoverImage;
    }

    public void setCompanyCoverImage(String companyCoverImage) {
        this.companyCoverImage = companyCoverImage;
    }

    public long getReviewsNumber() {
        return reviewsNumber;
    }

    public void setReviewsNumber(long reviewsNumber) {
        this.reviewsNumber = reviewsNumber;
    }

    public long getEngagements() {
        return engagements;
    }

    public void setEngagements(long engagements) {
        this.engagements = engagements;
    }

    public long getSells() {
        return sells;
    }

    public void setSells(long sells) {
        this.sells = sells;
    }

    public long getVisits() {
        return visits;
    }

    public void setVisits(long visits) {
        this.visits = visits;
    }
}
